<h2 id="what-is-ios-development" tabindex="-1"><a class="header-anchor" href="#what-is-ios-development" aria-hidden="true">#</a> What is iOS development ?</h2>
<p>iOS development consists of developing applications that target iPhones, iPads, macOS, iWatch and Apple TV.
There are many ways to achieve this:</p>
<ul>
<li>Using the official frameworks and tools developped by Apple</li>
<li>Using 3rd party frameworks and tools such as Capacitor and FLutter</li>
