<h2 id="qu-est-ce-que-ios-development" tabindex="-1"><a class="header-anchor" href="#qu-est-ce-que-ios-development" aria-hidden="true">#</a> Qu'est-ce que iOS development ?</h2>
<p>Le développement iOS consiste à développer des applications qui ciblent les iPhones, iPads, macOS, iWatch et Apple TV.
Il y a plusieurs façons d'y parvenir :</p>
<ul>
<li>En utilisant des frameworks et outils officiels développés par Apple</li>
<li>En utilisant frameworks et outils tiers tels que Capacitor et FLutter</li>
</ul>
<p>Cette formation se concentre sur le développement iOS en utilisant les outils et frameworks officiels proposés par Apple. Notre pile de développement consistera sur les éléments suivants :</p>
<ul>
<li>Language: Swift</li>
<li>Framework: SwiftUI</li>
<li>IDE: Xcode</li>
</ul>
<p>En plus de cela, il est également possible d'exploiter le langage Swift afin de développer des applications de console et des serveurs sur Windows, Linux, macOS.</p>
<h2 id="historique" tabindex="-1"><a class="header-anchor" href="#historique" aria-hidden="true">#</a> Historique</h2>
<p>Les premiers jours du développement iOS ont été réalisés en utilisant le langage Objective-C, le framework UIKit et -le bon vieux- Xcode.
Cet écosystème était basique mais assez puissant et permettait de développer des applications étonnantes.
Les mises à jour continues d'Apple ont rendu le développement beaucoup plus convivial pour les développeurs.
Par exemple, la gestion de la mémoire est devenue automatique (grâce à ARC) et le système de mise en page est devenu capable de s'adapter à différentes tailles d'écran.</p>
<p>Lors de la WWDC 2014, Apple a annoncé le langage Swift comme remplaçant moderne de l'Objective-C.
Suite à cela, Apple a annoncé lors de la prochaine WWDC SwiftUI en remplacement de UIKit.</p>
<p><img src="@source/assets/swiftui-framework-wwdc.jpg" alt="swiftui"></p>
<p>À partir de 2021, la majorité des nouveaux projets iOS utilisent Swift et SwiftUI avec UIKit comme solution de secours.</p>
