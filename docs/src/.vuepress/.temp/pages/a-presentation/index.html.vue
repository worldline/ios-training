<li>IDE: Xcode</li>
</ul>
<p>In addition to that, It is also possible to leverage the Swift language in order to develop console apps and servers on Window, Linux, macOS.</p>
<h2 id="historty" tabindex="-1"><a class="header-anchor" href="#historty" aria-hidden="true">#</a> Historty</h2>
<p>The early days of iOS development were done using the Objective-C language, the UIKit UI Framework and -the good old- Xcode.
This ecosystem was basic but quite powerful and allowed to develop amazing apps.
The continuous updates from Apple made development much more developer friendly.
For example, memory management became automatic (thanks to ARC) and the layout system became capable of adapting to different screen sizes.</p>
<p>In WWDC 2014, Apple announced the Swift language as a modern replacement to Objective-C.
Following that, apple announced during the next WWDC SwiftUI as the replacement for UIKit.</p>
<p><img src="@source/assets/swiftui-framework-wwdc.jpg" alt="swiftui"></p>
<p>As of 2021, the majority of new iOS projects use Swift and SwiftUI with UIKit as a fallback.</p>
